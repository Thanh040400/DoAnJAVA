    private void Tong() {
        DecimalFormat x= new DecimalFormat("###,###,###");
        int tong = 0;
        for (int i = 0; i < jTableHoaDon.getRowCount(); i++) {
            tong += Integer.parseInt(jTableHoaDon.getValueAt(i, 2).toString());
        }
        jLabelTongDoanhThu.setText("TỔNG DOANH THU :"+ " " + x.format(tong)+ " " + "VND");
    }
