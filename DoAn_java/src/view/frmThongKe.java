    private void hienThiTableHoaDon() {
        try {
            dtmHD.setRowCount(0);
            conn=KetNoiCSDL();
            String sql="select MaHoaDon,NgayXuatHoaDon,Tongtien\n" +
                        "from HOADON \n";
            ps=conn.prepareStatement(sql);
            rs=ps.executeQuery();
            while(rs.next()){
                Vector<Object> vec=new Vector<>();
                vec.add(rs.getInt("MaHoaDon"));
                vec.add(rs.getDate("NgayXuatHoaDon"));
                vec.add(rs.getLong("Tongtien"));
                dtmHD.addRow(vec);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
